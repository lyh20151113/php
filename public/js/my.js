
$(function(){
	
	$('#user_menu_a').mouseover(function(){
		
		$('#user_menu').show();
	});
	$('#user_menu_a').mouseout(function(){
	
		
		$('#user_menu').hide();
	
	});
	
	$('#user_menu').mouseout(function(){
		
		$('#user_menu').hide();
	});
$('#user_menu').mouseover(function(){
		
		$('#user_menu').show();
	});
});


